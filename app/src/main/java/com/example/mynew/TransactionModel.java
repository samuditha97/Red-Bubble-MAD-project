package com.example.mynew;

public class TransactionModel {
    private String id, amount, note, type, date;

    public TransactionModel() {
    }

    public TransactionModel(String id, String amount, String note, String type, String date) {
        this.id = id;
        this.amount = amount;
        this.note = note;
        this.type = type;
        this.date = date;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
